#include "../libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	size_t	len;

	if (fd > 0 && s != NULL)
	{
		len = ft_strlen(s);
		if (write(fd, s, len) == -1)
			return ;
	}
}
